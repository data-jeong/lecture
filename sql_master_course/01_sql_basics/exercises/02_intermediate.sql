-- 문제 1: 가장 비싼 상품 TOP 5를 조회하세요.
-- 상품명, 카테고리, 가격을 표시하세요.



-- 문제 2: 재고(stock_quantity)가 10개 미만인 Electronics 카테고리 상품을 조회하세요.
-- 재고가 적은 순으로 정렬하세요.



-- 문제 3: 국가별 고객 수를 조회하되, 고객이 5명 이상인 국가만 표시하세요.
-- 고객 수가 많은 순으로 정렬하세요.



-- 문제 4: 부서별 최고 급여와 최저 급여, 그리고 급여 차이를 계산하세요.
-- 급여 차이가 큰 순으로 정렬하세요.
-- 힌트: MAX - MIN을 계산



-- 문제 5: 각 카테고리에서 가장 비싼 상품의 정보를 조회하세요.
-- 힌트: 서브쿼리를 사용하거나, 각 카테고리별 MAX 가격을 먼저 구하세요.



-- 문제 6: 평균 급여보다 높은 급여를 받는 직원들의 정보를 조회하세요.
-- 부서명과 함께 표시하고, 급여가 높은 순으로 정렬하세요.



-- 문제 7: 각 도시별 고객 수를 구하고, 2개 이상의 고객이 있는 도시만 표시하세요.
-- 국가명도 함께 표시하세요.



-- 문제 8: 직원 수가 가장 많은 부서 TOP 3를 조회하세요.
-- 부서명, 직원 수, 평균 급여를 표시하세요.



-- 문제 9: 재고 금액(price * stock_quantity)이 가장 높은 상품 10개를 조회하세요.
-- 상품명, 재고 수량, 단가, 총 재고 금액을 표시하세요.



-- 문제 10: 2019년도에 입사한 직원들의 현재 평균 급여와 
